                    réalité, cela peut être compris par n'importe qui avec un minimum de connaissances en informatique
                    et en mathématiques. Le processus de minage peut être réalisé à l'aide d'un ordinateur personnel,
                    ce qui rend cette activité accessible à presque tout le monde.
                </p>
