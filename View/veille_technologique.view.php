                <h1 class="title title-secondary">
                    QU’EST-CE QUE LA CRYPTOMONNAIE ET COMMENT FONCTIONNE T’ELLE ?
                </h1>

                <p class="description">
                    La cryptomonnaie est une monnaie numérique qui utilise la cryptographie pour sécuriser et vérifier
                    les transactions et contrôler la création de nouvelles unités. Les transactions sont enregistrées
                    sur une blockchain, une base de données distribuée et décentralisée. La cryptomonnaie peut être
                    utilisée pour effectuer des transactions en ligne ou être conservée comme une forme
                    d'investissement. Cependant, elle est également utilisée dans certaines activités criminelles.
