                <h1 class="title title-secondary">
                    COMMMENT FONCTIONNE LE MINAGE D’UNE CRYPTO-MONNAIE ?
                </h1>

                <p class="description">
                    Le minage de crypto-monnaies, tel que celui du Bitcoin, consiste à effectuer des vérifications
                    complexes des transactions en utilisant la puissance de calcul de son ordinateur pour trouver la
                    solution du problème mathématique lié à une formule cryptologique précise. Cette preuve de travail
                    permet de valider la transaction et récolter une commission ainsi que de nouveaux Bitcoins.
                    Cependant, le minage du Bitcoin devient de plus en plus complexe, car il est conçu pour être de
                    plus en plus difficile, et cette tâche est effectuée par des fermes de serveurs de plus en plus
                    grandes.
