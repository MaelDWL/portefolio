                <h1 class="title title-secondary">Cryptomonnaie</h1>

                <p>
                    Le minage de cryptomonnaie est un processus de validation de transactions et de création de
                    nouveaux blocs dans une Block Chain en utilisant des ressources informatiques. Les mineurs
                    résolvent des équations mathématiques complexes pour vérifier les transactions et ajouter des
                    blocs à la chaîne de blocs existants. En récompense de leur travail, les mineurs reçoivent des
                    jetons de la cryptomonnaie en question.
                </p>
